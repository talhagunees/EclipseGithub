package introo;

public class LoopDemo {

	public static void main(String[] args) {
		// For
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);

		}
		System.out.println("For Döngü Bitti");

		int i = 1;
		// While
		while (i <= 10) {
			System.out.println(i);
			i++;

		}
		System.out.println("While Dögüsü Bitti");

	}

}
